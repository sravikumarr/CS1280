#No Arguments, No Return Value:
def greet():
    print("Hello!")
greet()
#No Arguments, With Return Value:
def pi():
    return 3.14
print(pi())
#With Arguments, No Return Value:
def display(message):
    print(message)
display("Hello, World!")
#With Arguments, With Return Value:
def add(a, b):
    return a + b
print(add(5, 7))
